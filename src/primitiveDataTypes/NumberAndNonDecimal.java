package primitiveDataTypes;

public class NumberAndNonDecimal {

	public static void main(String[] args) 
	{
		// 1 byte use
		// Variable declaration
		byte automation_testor; //byte is a data type and automation testor is variable
		int a;
	
		// variable initialization
		automation_testor= 11; //11 is value of testor and = called as assighment operator
		a= 50;
		
		// usage (printing)
		System.out.println("automation_testor");
		System.out.println(automation_testor);
		System.out.println(a);
		System.out.println("a");
				
			
		// 2 int use
		//variable declaration my mobile prize is 25000
		int prize;
		
		//variable assigning
		prize=25000;
		
		//usage
		System.out.println("my mobile prize is "+prize);
		
		// 3 long use
		//variable declaration flat value is 25000000
		long value;
		
		// variable assigning
		value= 25000000;
		
		//usage
		System.out.println("value");
		System.out.println(value);
		
		// long use with use of l
		
		long money;
		
		money=3300000000000l;
				
		System.out.println(money);
		
		
		

	}

}
