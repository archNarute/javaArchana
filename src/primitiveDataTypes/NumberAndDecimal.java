package primitiveDataTypes;

public class NumberAndDecimal
   
{public static void main(String[] args)
{
	 
	//float and double used for number and variable
	// use of float ( used upto 6 decimal after . )
	
	// variable declaration (weight= 45.50)
	float weight;
	
	// variable assigning
	weight=45.50f; // use f cumpalsary
	
	// usage
	System.out.println("my weight is "+weight);
	
	
	// use of double
	// sugar weight 99.12345678912345
	
	double sugarWeight;
	
	sugarWeight=99.12345678912345d;
	
	System.out.println("sugarWeight is "+sugarWeight);
	 
}

}
