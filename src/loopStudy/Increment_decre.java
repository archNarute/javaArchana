package loopStudy;

public class Increment_decre {

	public static void main(String[] args)
	{
		int a=10;
		System.out.println(a++);
		System.out.println(a);
		a+=1;
	
		System.out.println(a);
		System.out.println("==========================");
		//a--;
		System.out.println(a--);
		System.out.println(a);
		System.out.println("==========================");
		
		
		//++a;
		System.out.println(++a);
		System.out.println("==========================");
		--a;
		System.out.println(a);
		
		//a=a+1 = +1;
		//a=a*1;
		//a=a-1;
		//a=a/1;
		
		//a=+1;
        
	}

}
