package loopStudy;

public class For_Loop_Case {

	public static void main(String[] args)
	{
		//we want even no series between 100 to 120
		//Initialization    condition     updation    
         //  x=100           x<=120        x=x+2
		
		for(int x=100; x<=120; x=x+2)
		{
			System.out.println(x);
		}
		System.out.println("==================================");
		
		
		//we want number multiple to 3 between 3 to 30
		
		for(int a=3; a<=30; a=a+3)
		{
			System.out.println(a);
		}
		
		
		
	}

}
