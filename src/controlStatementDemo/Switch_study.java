package controlStatementDemo;

public class Switch_study {

	public static void main(String[] args)
	{
		int month=5;
			switch (month) 
			{
			case 1:System.out.println("month is january");
			break;
			case 2:System.out.println("month is feb");
			break;
			case 3:System.out.println("month is march");
			break;
			case 4:System.out.println("month is april");
			break;
			case 5:System.out.println("month is may");
			break;
			case 6:System.out.println("month is june");
			break;
			default:System.out.println("please enter valid month code");
			break;
			}
		
		
		
	}
}
